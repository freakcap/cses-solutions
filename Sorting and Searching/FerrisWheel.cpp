void solve()
{
    ll n, x; cin >> n >> x;
    vector<ll>arr(n);
    for (auto& v : arr) cin >> v;
    sort(arr.begin(), arr.end());
    int i = 0, j = arr.size() - 1;
    ll ans = 0;
    while (i <= j)
    {
        if (arr[j] > x)
        {
            j--;
        }
        else if (arr[j] + arr[i] > x)
        {
            ans++;
            j--;
        }
        else {
            i++;
            j--;
            ans++;
        }
    }
    cout << ans;
}

